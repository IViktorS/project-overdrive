using UnityEngine;

/// <summary>
/// Содержит характеристики и параметры волны (урон, геометрию, свойства пробития).
/// </summary>
[System.Serializable]
public class WaveStats
{
    [Header("Урон")]
    /// <summary>
    /// Базовый урон, который наносит волна при попадании.
    /// </summary>
    public float damage = 25f;

    /// <summary>
    /// Потеря урона на всю дистанцию. Значение 0 означает отсутствие потери урона, 
    /// значение 1 означает полную потерю урона на максимальной дистанции.
    /// </summary>
    [Range(0f, 1f)]
    public float distanceFalloff = 0f;

    [Header("Геометрия")]
    /// <summary>
    /// Визуальная ширина волны в юнитах.
    /// Не влияет на размер хитбокса.
    /// </summary>
    public float visualWidth = 0.8f;

    /// <summary>
    /// Расстояние до исчезновения волны в юнитах. 
    /// После этого расстояния волна перестает наносить урон.
    /// </summary>
    public float length = 8f;

    /// <summary>
    /// Скорость волны.
    /// </summary>
    public float speed = 14f;

    /// <summary>
    /// Зубчатость фронта волны. Значение 0 означает идеально ровный фронт.
    /// Задел под будущие визуальные эффекты, например Distortion.
    /// </summary>
    [Range(0f, 1f)]
    public float contourAmplitude = 0f;

    [Header("Пробитие")]
    /// <summary>
    /// Ограничение по количеству пораженных врагов (сколько врагов волна может пробить насквозь). 
    /// Значение 0 означает отсутствие пробития (волна исчезает после первого попадания).
    /// </summary>
    public int pierceCount = 0;

    /// <summary>
    /// Потеря урона на каждого последующего врага, через которого пробивается волна.
    /// </summary>
    public float pierceFalloff = 0.3f;

    /// <summary>
    /// Создает копию текущих характеристик волны.
    /// Используется для того, чтобы модификаторы (педали) применяли эффекты к копии параметров, а не к базовому шаблону.
    /// </summary>
    /// <returns>Новый экземпляр <see cref="WaveStats"/> с такими же значениями.</returns>
    public WaveStats Clone() => (WaveStats)MemberwiseClone();
}
